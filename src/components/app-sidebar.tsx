export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const [user, setUser] = React.useState({
    name: "",
    email: "",
    avatar: "",
  })

  React.useEffect(() => {
    async function loadMemberData() {
      try {
        const memberstack = await getMemberstack()
        const { data: member } = await memberstack.getCurrentMember()

        if (member) {
          const firstName = member.customFields?.["first-name"] || ""
          const lastName = member.customFields?.["last-name"] || ""
          const fullName = [firstName, lastName].filter(Boolean).join(" ") || member.auth?.email || "User"

          setUser({
            name: fullName,
            email: member.auth?.email || "",
            avatar: member.profileImage || "",
          })
        }
      } catch (error) {
        // Silently handle errors - user data will remain empty
      }
    }

    loadMemberData()
  }, [])

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:!p-1.5"
            >
              <a href="#">
                <ArrowUpCircleIcon className="h-5 w-5" />
                <span className="text-base font-semibold">Acme Inc.</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={data.navMain} />
        <NavDocuments items={data.documents} />
        <NavSecondary items={data.navSecondary} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        <NavUser user={user} />
      </SidebarFooter>
    </Sidebar>
  )
}
